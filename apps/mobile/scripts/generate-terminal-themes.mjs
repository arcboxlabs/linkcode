// Regenerates src/constants/terminal-themes.generated.ts from restty's
// builtin themes: the mobile bundle stays restty-free (browser/WASM package)
// while matching the desktop terminal's colors exactly.
// Run from apps/mobile: node scripts/generate-terminal-themes.mjs && pnpm -w format
import { writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, join } from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

// The desktop terminal's curated scheme list (packages/presentation/ui shell/terminal/prefs).
const SCHEMES = [
  'GitHub Dark Default',
  'GitHub Light Default',
  'Dracula',
  'Nord',
  'Catppuccin Mocha',
  'Catppuccin Latte',
  'One Half Dark',
  'One Half Light',
  'Gruvbox Dark',
];

const hex = (color) =>
  color
    ? `#${[color.r, color.g, color.b].map((v) => v.toString(16).padStart(2, '0')).join('')}`
    : undefined;

async function main() {
  const require = createRequire(import.meta.url);
  const resttyEntry = require.resolve('restty');
  const resttyVersion = require(join(dirname(resttyEntry), '..', 'package.json')).version;
  const { getBuiltinTheme } = await import(resttyEntry);

  const themes = Object.fromEntries(
    SCHEMES.map((name) => {
      const { colors } = getBuiltinTheme(name);
      return [
        name,
        {
          background: hex(colors.background),
          foreground: hex(colors.foreground),
          cursorColor: hex(colors.cursor),
          selectionBackground: hex(colors.selectionBackground),
          selectionForeground: hex(colors.selectionForeground),
          // ANSI 0-15 only: entries 16-255 are the standard xterm cube ghostty
          // derives itself.
          palette: colors.palette.slice(0, 16).map(hex),
        },
      ];
    }),
  );

  const out = `// Generated by scripts/generate-terminal-themes.mjs from restty@${resttyVersion}
// builtin themes — do not edit by hand; re-run the script after a restty bump.
import type { TerminalTheme } from 'expo-libghostty';

export const TERMINAL_THEME_NAMES = ${JSON.stringify(SCHEMES, null, 2)} as const;

export type TerminalThemeName = (typeof TERMINAL_THEME_NAMES)[number];

export const TERMINAL_THEMES: Record<TerminalThemeName, TerminalTheme> = ${JSON.stringify(themes, null, 2)};
`;

  const target = join(
    dirname(fileURLToPath(import.meta.url)),
    '..',
    'src',
    'constants',
    'terminal-themes.generated.ts',
  );
  writeFileSync(target, out);
  console.log(`wrote ${target} (restty@${resttyVersion})`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
